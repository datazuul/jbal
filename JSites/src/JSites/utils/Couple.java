package JSites.utils;

public class Couple {
	private Object obj1;
	private Object obj2;
	
	public Couple(Object o1, Object o2){
		obj1 = o1;
		obj2 = o2;
	}

	public Object getObj1() {
		return obj1;
	}

	public void setObj1(Object obj1) {
		this.obj1 = obj1;
	}

	public Object getObj2() {
		return obj2;
	}

	public void setObj2(Object obj2) {
		this.obj2 = obj2;
	}
}
